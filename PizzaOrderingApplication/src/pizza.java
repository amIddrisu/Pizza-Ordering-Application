
public class pizza {
	//creating an instance variable for pizza
	String name;
	String size;
	int price;
	
	//getters 
	public String getName() {
		return name;
	}
	
	public String getSize() {
		return size;
	}
	
	public int getPrice() {
		return price;
	}
	
	
	
	//setters
	public void setName(String name) {
		this.name = name;
	}
	
	public void setSize(String size) {
		this.size = size;
	}
	
	public void setPrice(int price) {
		this.price = price;
	}

}
